            <h3><b>Wp Custom Meta Box Plugin will help you to Show Meta Box on Front-end by Following way</b></h3>
            <ul>
                <li> Create Custom Metabox from admin panel from Add New Wp Custom Metabox section.</li>
                <li> Create new Metabox by Adding Meta Box Label, Field Type from drop down <b>(Textbox, Textarea, Drop Down, Check Box, Radio Button)</b>, 
                    according to your selected Field Type, fill option below Like Meta Field Type for <b>(Textbox)</b>, Meta Field Title, Meta Field Value 
                    for <b>(Check Box, Radio Button,Drop Down)</b>, Meta Field Post Type, Meta Field Required, Meta Field Place Holder 
                    for <b>(Textbox, Text Area)</b>, Meta Field Min Length for <b>(Textbox)</b>, Meta Field Max Length for <b>(Textbox)</b> Status status 
                    as 'Available' OR Not Available </li>
                <li> Admin will get that Meta Box on (Posts, Pages) as per selection of Meta Field Post Type.</li>
                <li> Go to particular Post or Page and put data in meta box and save it.</lip>
                <li> Saved Meta Box data display on frontend of that Post or Page.</li>
            </ul>
